function parseLineWords(
  arLine: string,
  wordDetails: { ml: string; en: string; irab: string; root?: string }[]
) {
  const words = arLine.trim().split(/\s+/);
  return words.map((w, idx) => {
    const detail = wordDetails[idx] || {
      ml: "അർത്ഥം",
      en: "Meaning",
      irab: "كلمة من البيت الشعري",
    };
    return {
      ar: w,
      ml: detail.ml,
      en: detail.en,
      irab: detail.irab,
      root: detail.root
    };
  });
}

